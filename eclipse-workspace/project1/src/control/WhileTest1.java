package control;

public class WhileTest1 {
	public static void main(String[] args) {
		int a = 0;
		while (a<=3) {
			System.out.println(a + ":황수연");
			a++;
		}
		System.out.println("종료");
		
		int b = 0;
		while (b<=30){
			System.out.println(b + ":김기훈");
			b+=10;
		}
		System.out.println("종료");
	}
}
